package com.snek.frameworklib.data_types.graphics;




/**
 * Defines the horizontal alignment of a UI element.
 * @since v1.1.0
 */
public enum AlignmentX {

    /** The element is not horizontally aligned and can move freely.                       */ NONE,
    /** The left edge  of the element is always aligned with the left edge of its parent.  */ LEFT,
    /** The center     of the element is always aligned with the center of its parent.     */ CENTER,
    /** The right edge of the element is always aligned with the right edge of its parent. */ RIGHT;
}
