package com.snek.frameworklib.data_types.graphics;




/**
 * Defines the vertical alignment of a UI element.
 */
public enum AlignmentY {
    /** The element is not vertically aligned and can move freely.                           */ NONE,
    /** The top edge    of the element is always aligned with the top edge    of its parent. */ TOP,
    /** The center      of the element is always aligned with the center      of its parent. */ CENTER,
    /** The bottom edge of the element is always aligned with the bottom edge of its parent. */ BOTTOM;
}
